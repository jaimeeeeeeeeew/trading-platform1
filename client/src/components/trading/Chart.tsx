import { useEffect, useRef } from 'react';
import { useTrading } from '@/lib/trading-context';
import { useToast } from '@/hooks/use-toast';

declare global {
  interface Window {
    TradingView: any;
  }
}

export default function Chart() {
  const container = useRef<HTMLDivElement>(null);
  const widget = useRef<any>(null);
  const { currentSymbol } = useTrading();
  const { toast } = useToast();

  useEffect(() => {
    console.log('ðŸ”„ Iniciando efecto para cargar TradingView');
    if (!container.current) {
      console.error('âŒ Container ref no disponible');
      return;
    }

    const script = document.createElement('script');
    script.src = 'https://s3.tradingview.com/tv.js';
    script.async = true;

    script.onload = () => {
      console.log('âœ… Script de TradingView cargado');
      console.log('TradingView disponible:', !!window.TradingView);

      if (!window.TradingView) {
        console.error('âŒ TradingView no disponible en window');
        return;
      }

      try {
        console.log('ðŸŽ¯ Intentando crear widget de TradingView');
        console.log('ID del contenedor:', container.current!.id);

        widget.current = new window.TradingView.widget({
          container_id: container.current!.id,
          width: "100%",
          height: "100%",
          symbol: "BINANCE:BTCUSDT",
          interval: "1",
          timezone: "Etc/UTC",
          theme: "dark",
          style: "1",
          locale: "es",
          enable_publishing: false,
          allow_symbol_change: true,
          save_image: true,
        });

        console.log('âœ… Widget creado exitosamente');
        console.log('Widget actual:', widget.current);

        // Verificar si el widget tiene el mÃ©todo onChartReady
        console.log('MÃ©todos del widget:', Object.keys(widget.current));

        if (typeof widget.current.onChartReady === 'function') {
          console.log('ðŸ“Š Configurando onChartReady');
          widget.current.onChartReady(() => {
            console.log('ðŸ“Š Chart estÃ¡ listo!');
            console.log('Objeto widget dentro de onChartReady:', widget.current);
          });
        } else {
          console.error('âŒ onChartReady no es una funciÃ³n');
        }

      } catch (error) {
        console.error('âŒ Error al crear widget:', error);
        toast({
          title: "Error",
          description: "No se pudo inicializar el grÃ¡fico",
          duration: 3000,
        });
      }
    };

    script.onerror = (error) => {
      console.error('âŒ Error al cargar script de TradingView:', error);
      toast({
        title: "Error",
        description: "No se pudo cargar TradingView",
        duration: 3000,
      });
    };

    document.head.appendChild(script);
    console.log('âœ… Script agregado al head');

    return () => {
      if (document.head.contains(script)) {
        console.log('ðŸ§¹ Limpiando script de TradingView');
        document.head.removeChild(script);
      }
    };
  }, [currentSymbol]);

  return (
    <div className="w-full h-full rounded-lg overflow-hidden border border-border bg-card">
      <div
        id="tradingview_widget"
        ref={container}
        className="w-full h-full"
      />
    </div>
  );
}